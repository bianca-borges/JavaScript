/*Operadores aritméticos*/

//concatenação e adição
const num1 = '5';
const num2 = 10;
console.log(num1 + num2);

//subtração
const num3 = 5;
const num4 = 10;
console.log(num3 - num4);

//multiplicação
const num5 = 5;
const num6 = 10;
console.log(num5 * num6);

//potenciação
const num9 = 2;
const num10 = 10;
console.log(num9 ** num10);

//divisão
const num7 = 5;
const num8 = 10;
console.log(num7 / num8);

//modulo
const num11 = 5;
const num12 = 10;
console.log(num11 % num12);

//incremento e decremento sempre com let
let contador = 1;
contador ++;
contador++;
contador++;
contador--;
console.log(contador);